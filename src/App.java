public class App {
    static void printArray(int array[])
    {
        int n = array.length;
        for (int i=0; i<n; ++i)
            System.out.print(array[i] + " ");
        System.out.println();
    }
    public static void main(String[] args) throws Exception {
        System.out.println("Bienvenidos a 'Taller de Algoritmos y Estructura de Datos I'");
    }
}
